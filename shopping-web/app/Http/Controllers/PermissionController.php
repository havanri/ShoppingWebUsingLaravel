    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        DB::beginTransaction();
        try {
            $permissionParent=Permission::create([
                'name'=>$request->module_parent,
                'display_name'=>$request->module_parent,
                'parent_id'=>0,
                'key_code'=>''
            ]);
            foreach($request->module_child as $moduleChildItem){
                Permission::create([
                    'name'=>$moduleChildItem,
                    'display_name'=>$moduleChildItem,
                    'parent_id'=>$permissionParent->id,
                    'key_code'=>$request->module_parent.'_'.$moduleChildItem
                ]);
            }
            DB::commit();
            return redirect('/home');
        } catch (\Throwable $ex) {
            //throw $th;
            DB::rollBack();
            Log::error('Message '.$ex->getMessage().' Line '.$ex->getLine());
        }
    }

